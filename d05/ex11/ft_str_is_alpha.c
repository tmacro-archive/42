int		ft_str_is_alpha(char *str)
{
	int alpha;

	alpha = 1;
	if (!*str)
		return (0);
	while (*str != 0)
	{
		if (!(*str >= 65 && *str <= 90))
		{
			if (!(*str >= 97 && *str <= 122))
			{
				if (*str != '\0')
				{
					alpha = 0;
					break ;
				}
			}
		}
		str++;
	}
	return (alpha);
}
