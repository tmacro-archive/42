int		ft_str_is_numeric(char *str)
{
	int num;

	num = 1;
	if (!*str)
		return (0);
	while (*str != 0)
	{
		if (!(*str >= 48 && *str <= 57))
		{
			if (*str != '\0')
			{
				num = 0;
				break ;
			}
		}
		str++;
	}
	return (num);
}
