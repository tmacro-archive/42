#include <stdio.h>

int match(char *s1, char *s2)
{
	if (s1 == '\0' && *s2 != '*')
		return (0);
	while (*s1 && *s2)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		if (*s1 == '*')
			s2++;
		if (*s2 == '*')
			s1++;
		if (*s1 != *s2)	
			return (0);
	}
	return (1);
		
}

